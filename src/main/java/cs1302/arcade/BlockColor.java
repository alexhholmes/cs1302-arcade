package cs1302.arcade;

/**
 * Enum representing the colors of the Tetris game blocks.
 */
public enum BlockColor {
    /** SquareBlock */
    YELLOW,
    /** RightLBlock */
    ORANGE,
    /** LeftLBlock */
    DARK_BLUE,
    /** LineBlock */
    LIGHT_BLUE,
    /** RightZBlock */
    GREEN,
    /** LEFTZBlock */ 
    RED,
    /** TBlock */
    PURPLE,
    /** Background */
    BLACK
} // BlockColor
